let num
num = Number(prompt("Digite nº 0-9: "))

switch(num){
    case 0:
        alert("Zero")
    break

    case 1:
        alert("Um")
    break

    case 2:
        alert("Dois")
    break

    case 3:
        alert("Três")
    break
    default:
        alert("Digite entre 0-9, Tanso(a)!")
    }